package com.agent.entities;

public class ProcessRequest {
	
	private String inRequest;
	private String inRequestPolicy;
	private String outRequest;
	private String outRequestPolicy;
	
	
	public String getInRequest() {
		return inRequest;
	}
	public void setInRequest(String inRequest) {
		this.inRequest = inRequest;
	}
	public String getInRequestPolicy() {
		return inRequestPolicy;
	}
	public void setInRequestPolicy(String inRequestPolicy) {
		this.inRequestPolicy = inRequestPolicy;
	}
	public String getOutRequest() {
		return outRequest;
	}
	public void setOutRequest(String outRequest) {
		this.outRequest = outRequest;
	}
	public String getOutRequestPolicy() {
		return outRequestPolicy;
	}
	public void setOutRequestPolicy(String outRequestPolicy) {
		this.outRequestPolicy = outRequestPolicy;
	}
		
	

}
